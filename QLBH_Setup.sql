CREATE TABLE BILL
(
	IDBILL INT IDENTITY PRIMARY KEY NOT NULL,
	USERNAME VARCHAR(100) FOREIGN KEY REFERENCES ACCOUNT(USERNAME),
	STATUS INT,
	DISCOUNT INT,
	TOTALPRICE FLOAT,
	DATE DATETIME

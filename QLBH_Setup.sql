CREATE PROC USP_REPORT1
AS
BEGIN
	SELECT IDBILL AS [SỐ HÓA ĐƠN], B.IDITEM AS [MÃ SẢN PHẨM], I.NAMEITEM AS [TÊN SẢN PHẨM], COUNT AS [SỐ LƯỢNG], COUNT * I.PRICE AS [THÀNH TIỀN]
	FROM BILLINFO B, ITEM I
	WHERE B.IDITEM = I.IDITEM AND IDBILL IN
	(
		SELECT IDBILL FROM BILL
		WHERE YEAR(DATE) = YEAR(GETDATE()) AND MONTH(DATE) = MONTH(GETDATE()) AND DAY(DATE) = DAY(GETDATE())
	)
END
GO

CREATE PROC USP_REPORT2
AS
BEGIN
	SELECT IDBILL AS [SỐ HÓA ĐƠN], USERNAME AS [NGƯỜI TẠO], TOTALPRICE AS [TỔNG CỘNG], DISCOUNT AS [GIẢM GIÁ], DATE AS [NGÀY TẠO]
	FROM BILL
	WHERE YEAR(DATE) = YEAR(GETDATE()) AND MONTH(DATE) = MONTH(GETDATE()) AND DAY(DATE) = DAY(GETDATE())
END
GO

CREATE PROC USP_REPORT3
AS
BEGIN
	SELECT DAY(DATE), MONTH(DATE), YEAR(DATE), SUM(TOTALPRICE)
	FROM BILL
	WHERE YEAR(DATE) = YEAR(GETDATE()) AND MONTH(DATE) = MONTH(GETDATE())
	GROUP BY YEAR(DATE), MONTH(DATE), DAY(DATE)
	ORDER BY DAY(DATE) DESC
END
GO


CREATE PROC USP_INSERTBILLINFO
@IDBILL INT, @IDITEM NVARCHAR(100) , @COUNT INT
AS
BEGIN
	DECLARE @ISEXITSBILLINFO INT
	DECLARE @ITEMCOUNT INT = 1

	SELECT @ISEXITSBILLINFO = IDBILLINFO, @ITEMCOUNT = B.COUNT FROM BILLINFO AS B WHERE IDBILL = @IDBILL AND IDITEM = @IDITEM

	IF(@ISEXITSBILLINFO > 0)
	BEGIN
		DECLARE @NEWCOUNT INT = @ITEMCOUNT + @COUNT
		IF(@NEWCOUNT > 0)
			UPDATE BILLINFO SET COUNT = @ITEMCOUNT + @COUNT WHERE IDITEM = @IDITEM AND IDBILL = @IDBILL -- thiếu idbill = @idbill
		ELSE
			DELETE BILLINFO WHERE IDBILL = @IDBILL AND IDITEM = @IDITEM
	END
	ELSE
	BEGIN
		IF(@COUNT < 0)
			GOTO END1
		ELSE
		BEGIN
		INSERT BILLINFO
		(
		IDBILL,
		IDITEM,
		COUNT
		)
		VALUES
		(
		@IDBILL,
		@IDITEM,
		@COUNT
		)
		END
	END
	END1: -- THEM 1 DONG CODE CHO HET END
END


GO


CREATE TABLE BILLINFO
(
	IDBILLINFO INT IDENTITY PRIMARY KEY NOT NULL,
	IDBILL INT FOREIGN KEY REFERENCES BILL(IDBILL),
	IDITEM NVARCHAR(100) FOREIGN KEY REFERENCES ITEM(IDITEM),
	COUNT INT
)

GO


